//WAP in c To Find Area of Rectangle.
#include<stdio.h>
int main()
{
    int l,b,area;
    printf("\nEnter Length:");
    scanf("%d",&l);
    printf("\nEnter Width:");
    scanf("%d",&b);
    area=l*b;
    printf("Area Of Rectangle=%d",area);
    return 0;
}
